public class Calculator {

    public int add(int a, int b){
        int sum;
        sum  = a+b;
        return sum;
    }

    public int subtract(int a, int b){
        int difference;
        difference = a - b;
        return difference;
    }

    public int multiply(int a, int b){
        int result;
        result = a * b;
        return result;
    }

    public double divide(double a, double b){
        double result;
        result = a/b;
        return result;
    }
}
